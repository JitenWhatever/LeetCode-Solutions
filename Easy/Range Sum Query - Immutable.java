class NumArray {

    int[] nums;
    public NumArray(int[] nums) {
        int len = nums.length;
        this.nums = nums;
       for (int index = 1; index < len; ++index) {
           nums[index] += nums[index - 1];
       }        
    }
    
    public int sumRange(int left, int right) {
        --left;
        if (left < 0) {
            return this.nums[right];
        }
        
        return this.nums[right] - this.nums[left];
    }
}

